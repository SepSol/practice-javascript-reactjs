// The 'window' object

// === PROPS === //
console.log(window); // outputs all the available functions and props

console.log(window.location); // outputs URL
console.log(window.document); // outputs HTML

console.log(window.outerWidth); // outputs width of entire browser
console.log(window.innerWidth); // outputs width of website in browser

console.log(window.outerHeight); // outputs height of entire browser
console.log(window.innerHeight); // outputs height of website in browser


// === FUNCTIONS === //
window.open('https://www.google.com/') // opens google in a new tab